#include "../libft.h"

char	*ft_strrchr(const char *str, char c)
{
	char	*s;

	s = (char *)str;
	while (*s)
		s++;
	s--;
	while (*s)
	{
		if (*s == c)
			return (s);
		s--;
	}
	return (0);
}
